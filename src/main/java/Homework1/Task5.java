package Homework1;

public class Task5 {
    public static void main(String[] args) {

        System.out.println("Printing the numbers from 1 to 100 in ascending order:");
        for(int i = 1; i <= 100; i ++){
            System.out.print(i + " ");
        }

        System.out.println();

        System.out.println("Printing the numbers from 1 to 100 in descending order:");
        for(int j = 100; j > 0; j --){
            System.out.print(j + " ");
        }
    }
}
